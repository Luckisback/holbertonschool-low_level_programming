#include "3-calc.h"

/**
 * op_add - a function that adds 2 numbers
 * @a: num1
 * @b: num2
 * Return: the result
 **/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - a function that subtracs 2 numbers
 * @a: num1
 * @b: num2
 * Return: the result
 **/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - a function that multiplies 2 numbers
 * @a: num1
 * @b: num2
 * Return: the result
 **/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - a function that divides 2 numbers
 * @a: num1
 * @b: num2
 * Return: the result
 **/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - a function that returns the remainder of a division of 2 numbers
 * @a: num1
 * @b: num2
 * Return: the result
 **/
int op_mod(int a, int b)
{
	return (a % b);
}
